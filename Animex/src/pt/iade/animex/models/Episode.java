package pt.iade.animex.models;

public class Episode {
	
	private int episode;
	private boolean visto;
	
	public Episode(int episode, boolean visto) {
		this.episode = episode;
		this.visto = visto;
	}

	public int getEpisode() {
		return episode;
	}

	public boolean isVisto() {
		return visto;
	}
	
	
	
	

}
